package day01;

public class Ex02 {
	public static void main(String[] args) {
		//Escape 문자
		//New line : 새로운 줄로 이동
		System.out.println("100\n200\n300\n"); 
		
		// Tab : 탭크기만큼 이동
		System.out.println("1\t1234567\t12345678\t1"); 
		System.out.println("이름\t나이"); 
		System.out.println("=============");
		System.out.println("홍길동\t20살");
		System.out.println("김계동\t30살");
		System.out.println("=============");
		
		// \" or \\ : " or \ 출력하게 함
		System.out.println("  \"  \\  ");
		
	}
}
